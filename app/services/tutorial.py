            "- Keyword off = filter မရှိ, ban = keyword ပါရင် skip, post = keyword ပါမှတင်။\n"
            "- Forward Rule ON = forwarded post မတင်။ OFF = forwarded post ကိုခွင့်ပြု။\n"
            "- Post Rule ON = video post/video ပါတဲ့ album ကို main candidate လုပ်ပြီး အရင် post ကို preview အဖြစ်တင်နိုင်။ OFF = post အားလုံး candidate ဖြစ်။\n"
