            "Common problems / FAQ\n"
            "- ဘာမှမတင်ဘူးဆိုရင် source မှန်လား, target မှာ shared session ပို့ခွင့်ရှိလား, rules တွေ block လုပ်နေလား စစ်ပါ။\n"
            "- Source resolve မရရင် public link/username မှန်လား, private invite link valid ဖြစ်လား စစ်ပါ။\n"
            "- Target ပို့မရရင် shared watcher account ကို target မှာ member/admin လိုအပ်နိုင်ပါတယ်။\n"
