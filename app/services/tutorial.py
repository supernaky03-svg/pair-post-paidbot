    return (
        "Quick setup\n"
        "1. Enter your OTP key.\n"
        "2. Tap Add Pair.\n"
        "3. Send pair number, source, scan amount, target, ads, post rule, and forward rule step by step.\n"
        "4. Confirm the summary and let the runtime watch your sources.\n\n"
        "Full setup guide\n"
        "- This bot reposts posts from a source channel to a target channel.\n"
        "- A source is where posts are read from.\n"
        "- A target is where reposts are sent to.\n"
        "- The whole service uses one shared watcher account. End users do not provide session strings.\n"
        "- Public sources are resolved and watched only. They are never joined.\n"
        "- Private sources need an invite link. The shared session account may join them when needed.\n"
        "- If an old source is no longer used by any active pair, the shared session account may leave it.\n"
        "- Pair number means your own slot number for one source -> target setup.\n"
        "- Scan amount defaults to 100 and also supports all.\n"
        "- Ads are links appended to outgoing text/captions.\n"
        "- Keyword off = no filter, ban = skip on match, post = repost only on match.\n"
        "- Forward Rule ON = skip forwarded content. OFF = allow forwarded content.\n"
        "- Post Rule ON = only video single posts or albums become main candidates, and the immediately previous post may be reposted as preview. OFF = all posts are candidates.\n"
        "- Check starts from stored last_processed_id.\n"
        "- Status shows your license, pairs, rules, and runtime warnings.\n"
        "- Back returns to the previous step. Cancel exits the full flow and returns to the main menu.\n\n"
        "Common problems / FAQ\n"
        "- If nothing is posting, check whether the source is valid, whether the target is writable by the shared session, and whether the rules are blocking content.\n"
        "- If a source cannot be resolved, verify the public username/link or the private invite link.\n"
        "- If target posting fails, the shared watcher account may need member/admin permissions there.\n"
        "- If access expires, use /start and enter a new OTP key."
    )
